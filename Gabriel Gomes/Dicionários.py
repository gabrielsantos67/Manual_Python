#Dicionários
# Define um dicionário com pares chave-valor representando itens e seus preços
D = {"arroz": 17.30, "feijão": 12.50, "carne": 23.90, "alface": 3.40}
# Imprime o dicionário D
print(D)
# Define um dicionário com pares chave-valor representando itens e seus preços novamente
D = {"arroz": 17.30, "feijão": 12.50, "carne": 23.90, "alface": 3.40}
# Imprime o valor associado à chave "carne" no dicionário D
print(D["carne"])

# Define um dicionário com pares chave-valor representando itens e seus preços
D = {"arroz": 17.30, "feijão": 12.50, "carne": 23.90, "alface": 3.40}
# Atualiza o valor associado à chave "carne" para 25.0
D["carne"] = 25.0
# Adiciona um novo par chave-valor "tomate": 8.80 ao dicionário D
D["tomate"] = 8.80
# Imprime o dicionário D após as atualizações e adições
print(D)

